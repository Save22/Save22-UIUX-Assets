                  <h2>Disruptive price comparison app wins Singtel’s 2011 “Most Valuable App in Asia” award</h2>
                  <h3>Lobangclub aims to bring price transparency to retail in Singapore</h3>
               </header></a>
             </div>
             <div class="content">
                  <p>Asia has always been seen as a copycat of innovation invented elsewhere.   A growing list of technology companies are hoping to change that perception.  Lobangclub iPhone app has won the “Asia’s Most Valuable App” award for 2011.  The upstart app that only launched last month beat out a field of over 150 apps to claim the top prize.</p>
                     
                  <p>The LobangClub iPhone app turns any iPhone into a barcode scanner using its camera, and then checks a database for prices that other users have uploaded. At a glance, a shopper can see if other shops nearby offer the same item at a lower price.</p>

                  <p>Co-Founder of the company, Guyi Shen, says that the company is “focused on giving power back to consumers”.  He claims that by collecting the product and price knowledge of thousands of individuals across the island, everyone benefits by enhancing price transparency for all products being sold.</p>

                  <p>Currently users are adding prices from an eclectic mix of products ranging from books to cigarettes.   But some real savings are being found, for example, during the course of 48 hours, users of the app entered prices for a bottle of Sunsilk Shampoo that ranged from $10.90 at NUTC to $5.70 at Swanston.</p>

                  <p>Mr Shen says that the app is doubling in users every week and currently stands at 8000 users in Singapore.</p>
