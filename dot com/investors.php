                  <h2>High-tech trend in shopping hits Singapore shores</h2>
                  <h3>Shoppers are using their smartphones as a weapon to bludgeon retailers into giving better prices, will this work in Singapore?</h3>
               </header></a>
             </div>
             <div class="content">
               
               <p> The mobile power shopper trend in retail shopping has hit Singaporean shores.   According to the <a href="http://www.nytimes.com/2009/12/18/technology/18mobile.html">New York Times</a>,  since the iPhone and Android smartphones have debuted, a growing horde of mobile power shoppers have been using their smartphones as a weapon to compare prices at the point of purchase and forcing retailers to match the lowest price.
                     
               <p> Singaporean company  Lobangclub releases an iPhone app that encourages Singaporeans to actively compare prices amongst local retailers.The app finds you the lowest price for products by relying on a community of shoppers who come across low prices and keying them into a community database.   Founder of Lobangclub Guyi Shen described it as a "Wikipedia for Singapore shopping".
                     
               <p> The user uses the iPhone’s camera to scan the barcode of the item.  The app recognises the information and displays a list of all the shops that carry the product and their prices.  You are then encouraged to verify if the price is accurate and contribute a new price at the store where you are.
                     
               <p> Joe Goh, a father from Bedok, recently used it to find some batteries for his camera.  A quick trip to the Jurong point shopping centre, revealed that out of all the stores within Jurong point there was an almost 30% difference in the cost of a pack of energiser AAA batteries ranging from $13.90 at Challenger to $10.90 at NTUC, "I saved $3 in the space of 5 seconds, just doing a quick scan, no need to be kiasu and walk all over the place, it’s all right there in your hand".

               <p> Not all are in favour of this trend, noted technology publisher <a href="http://radar.oreilly.com/tim/index.html">Tim O'reilly</a> is <a href="http://radar.oreilly.com/2009/12/why-using-shopsavvy-might-not.html">quoted as saying</a> "pursuit of the lowest price will hasten the demise of many retailers".

               <p> Lobangclub is a Singapore company that provides technology to help consumers make more informed purchasing decisions.</p>
